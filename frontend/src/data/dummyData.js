export function generateFallbackSeats(theaterId) {
  const rows = ["A", "B", "C", "D", "E", "F", "G", "H"];
  return rows.flatMap((row, rowIndex) =>
    Array.from({ length: 10 }, (_, seatIndex) => {
      const seatNumber = `${row}${seatIndex + 1}`;
      const numericId = Number(`${theaterId}${rowIndex + 1}${seatIndex + 1}`);

      return {
        id: numericId,
        seat_number: seatNumber,
        is_booked: (rowIndex + seatIndex + Number(theaterId)) % 9 === 0,
      };
    })
  );
}
